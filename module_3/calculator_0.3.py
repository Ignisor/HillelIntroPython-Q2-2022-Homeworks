    print('–ù–µ–≤—ñ–¥–æ–º–∞ –æ–ø–µ—Ä–∞—Ü—ñ—è', operation, '–æ–±–µ—Ä—ñ—Ç—å –æ–¥–Ω–µ –∑: +, -, *, /, +++')

